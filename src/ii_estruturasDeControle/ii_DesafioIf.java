package ii_estruturasDeControle;

public class ii_DesafioIf {
	public static void main(String[] args) {
		
		double nota = 5.3;
		
		// não usar ';' em estruturas de com controle (porém existe uma excessão)
		if (nota >= 9.0); {
			System.out.println("Estás no Quadro de Honra!");
			System.out.println("És muito Fera!!!");
		}
		
	}
	

}
